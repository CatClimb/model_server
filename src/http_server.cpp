void HttpServer::StopServer() {
    {
        std::lock_guard<std::mutex> lock(m_server_fd_mutex);
        if (!m_running)
            return;

        // Step 1: 设置停止标志位
        m_running = false;

        // Step 2: 关闭服务器套接字（触发accept解除阻塞）
        if (m_server_fd != -1) {
            shutdown(m_server_fd, SHUT_RDWR);
            close(m_server_fd);
            m_server_fd = -1;
        }
    }

    // Step 3: 唤醒所有工作线程
    m_cv.notify_all();

    // Step 4: 等待线程结束
    for (auto& t : m_workers) {
        if (t.joinable()) {
            t.join();
        }
    }
}
